=begin
  What do the following expressions evaluate to? 
=end


# Solution

=begin 
1. x = 2
This will evaluate and return 2.

2. puts x = 2
This will evaluate to nil as puts returns nil at the end of its execution.

3. p name = "Joe"
This will evaluate/return the string "Joe".

4. four = "four"
This will evaluate the string "four"

5. print something = "nothing"
This will evaluate to nil as print returns nil at the end of its execution.

=end
