=begin
  What is exception handling and what problem does it solve? 
=end 

# Solution
=begin
  Exception handling is the process of managing exceptions to programmed logic within a program during runtime. 
  When programs interact with the outside world unexpected inputs or configurations to inputs may be received - if a program is unable to manage these unexpected inputs then the program may crash.
  Therefore the benefit to exception handling is continuity - which in this context is reduction of a program crashing/stopping unexpectedly.   
=end